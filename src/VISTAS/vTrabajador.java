    /**
     * Luego de tomar el codigo del producto y la cantidad estos se envian para 
     * buscar el producto y hacer el respectivo calculo
     * Posteriormente se muestran los datos en la tabla
     * @param cod
     * @param can 
     */
    public void datostabla(int cod,int can) {
        
        gestion ad = new gestion();
        for (productoDAO dat : ad.listarProducto(cod)) {
            Object ayuda[] = new Object[6];
            ayuda[0] = dat.getIntID();
            ayuda[1] = dat.getStrnombreProducto();
            ayuda[2] = can;
            ayuda[3] = dat.getDblPrecioProducto();
            ayuda[4] = dat.getDblPrecioProducto()* can;
            
            if(dat.getIntStock()<=0){
                JOptionPane.showMessageDialog(null, "Producto agotado");
                
                break;
            }else if((dat.getIntStock())<can){
                JOptionPane.showMessageDialog(null, "Articulo insuficiente. Cantidad en existencia: "+dat.getIntStock());
                break;
            }
            t1.addRow(new Object[]{
                ayuda[0],ayuda[1],ayuda[2],ayuda[3],ayuda[4]
            });
            ad.modificarProducto(cod, can);
        }
        jTable1.setModel(t1);
        
        codpro.setText("");
        canpro.setText("");
    }
    
    /**
     * Suma toda la columna de subtotales y lo muestra en el text field designado
     * @return float
     */
    private float suma(){
       
        int c = jTable1.getRowCount();
        float suma = 0;
        for (int i = 0; i < c; i++) {
            suma += Float.parseFloat(jTable1.getValueAt(i, 4).toString());
        }
        
        total.setText(String.valueOf(suma));
        return suma;
    }

    /**
     * Toma todos los datos que necesita para la creacion de la factura
     * Lanza excepciones en caso de que algo salga mal 
     * @throws FileNotFoundException
     * @throws DocumentException
     * @throws IOException 
     */
    private void jalarDato() throws FileNotFoundException, DocumentException, IOException{
        LocalDateTime datetime = LocalDateTime.now();
        int cajaV = caja;
        String noFactura = noFac.getText();
        String hora = String.valueOf(datetime);
        String nomVendedor = vendedo;
        String nit = nitF.getText();
        String nombreCliente = nombreF.getText();
        String fechaV = fecha.getText();
        //datos de la compra
        String ttl = total.getText();
        facturaPOO fp = new facturaPOO(cajaV,noFactura,nit,nombreCliente,fechaV,ttl);
        crearPDF cp = new crearPDF();
        LinkedList<facturaPOO> factura = new LinkedList<>();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String idProducto = String.valueOf(jTable1.getValueAt(i, 0));
            String nombreProducto = String.valueOf(jTable1.getValueAt(i, 1));
            String cantidad = String.valueOf(jTable1.getValueAt(i, 2));
            String precio = String.valueOf(jTable1.getValueAt(i, 3));
            String subtotal = String.valueOf(jTable1.getValueAt(i, 4));
            //enviar linkedlist por parametros
            factura.add(new facturaPOO(idProducto,nombreProducto,cantidad,precio,subtotal));
        }
        
        String a = factura+"\n"+noFactura+"\n"+fechaV+"\n"+hora+"\n"+nomVendedor+"\n"+cajaV+"\n"+nit+"\n"+nombreCliente+"\n"+ttl;
        cp.crearDocumento(factura,noFactura, fechaV,hora, nomVendedor, String.valueOf(cajaV),nit,nombreCliente,ttl);
        crearFactura(a);
    }
    
    /**
     * Envia las instrucciones para guardar la factura en la DB
     * @param a 
     */
    public void crearFactura(String a){
        crearPDF cpa = new crearPDF();
        gestion ra = new gestion();
        clientesPOO cp = ra.mostrarCliente(nitF.getText());
        if(cp != null){
            int asd = cp.getId();
            hisventasPOO hv = new hisventasPOO(asd,fecha.getText(),total.getText(),a);
        
            ra.crearhisVentas(hv);
        }else{
            System.out.println("ERROR en crear factura");
        }
        
        
    }
    
