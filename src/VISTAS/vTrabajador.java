    /**
     * Lista los datos de los vehiculos que estan en el taller, para que los 
     * trabajadores puedan verla 
     */
    private void datosEnProceso() {
        String columnas[] = {"ID", "Descripcion del Trabajo","Mecanico a Cargo","Dueño","Tipo de Vehiculo"};
        tEnProceso = new DefaultTableModel(null, columnas);
        EnProcesoImplementacion im = new EnProcesoImplementacion();
        for(EnProcesoDAO dat: im.listar()){
            Object ayuda[] = new Object[5];
            ayuda[0] = dat.getIntID();
            ayuda[1] = dat.getStrDescripcionProceso();
            ayuda[2] = dat.getStrNombreMecanico();
            ayuda[3] = dat.getStrNombreDueno();
            ayuda[4] = dat.getStrVehiculo();
            tEnProceso.addRow(ayuda);
        }
        //Pone el filtro en los encabezados de la tabla
        tablaEnProceso.setModel(tEnProceso);
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(tEnProceso);
        tablaEnProceso.setRowSorter(sorter);
          
    }
    
    /**
     * Metodo que se ejecuta para realizar un filtro en base a la informacion 
     * ingresada por el usuario
     * 
     * Los datos que coinciden con lo que el usuario ingreso, se guardan en un 
     * Combobox para usarlos posteriormente
     */
    private void datosObtener(){
        LinkedList<String> ca = new LinkedList<>();
        comboCliente.removeAllItems();
        a.clear(); b.clear(); c.clear(); d.clear(); ca.clear();
        String nombrev = nombreF.getText();
        String correov = correoF.getText();
        String nitv = nitF.getText();
        String generov = generoF.getText();
        gestion ev = new gestion();
        for (clientesPOO dat : ev.listarClientes(nombrev, nitv, correov, generov)) {   
            Object ayuda[] = new Object[4];
            ayuda[0] = dat.getNombre();
            ayuda[1] = dat.getNit();
            ayuda[2] = dat.getCorreo();
            ayuda[3] = dat.getGenero();
            
            ca.add(String.valueOf(ayuda[0]));
            Collections.sort(ca);  
            a.add(String.valueOf(ayuda[0]));
            b.add(String.valueOf(ayuda[1]));
            c.add(String.valueOf(ayuda[2]));
            d.add(String.valueOf(ayuda[3]));
        }
        for (String clientes: ca){
            comboCliente.addItem(clientes);
        }
    }
