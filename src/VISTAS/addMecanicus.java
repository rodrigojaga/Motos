    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Valida que todos los campos tengan texto o hayan sido elegidos 
        //En caso de que no, se pintaran de rojo indicando que hacen falta
        if(txtContraMeca.getText().isEmpty()||txtContraMeca.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "DEBE LLENAR TODOS LOS CAMPOS");
            Color ca = new Color(255,160,160);
            if(txtContraMeca.getText().equals("")){
                txtContraMeca.setBackground(ca);
            }
            if(txtNombreMeca.getText().equals("")){
                txtNombreMeca.setBackground(ca);
            }
                        
        }else{
            enviarDatos(txtNombreMeca.getText(),txtContraMeca.getText());
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

